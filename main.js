const languageOrder = ["nl", "en", "fr", "de", "ar"];

const languageMeta = {
  nl: { flag: "nlflag.svg", label: "Nederlands", alt: "Nederlands" },
  en: { flag: "uk.svg", label: "English", alt: "English" },
  fr: { flag: "fransflag.jpg", label: "Français", alt: "Français" },
  de: { flag: "duitsflag.svg", label: "Deutsch", alt: "Deutsch" },
  ar: { flag: "arabische taal.png", label: "العربية", alt: "العربية" },
};

function getValidLanguage(lang) {
  return languageOrder.includes(lang) ? lang : "nl";
}

function setLanguage(lang) {
  lang = getValidLanguage(lang);
  localStorage.setItem("siteLanguage", lang);
  translatePage(lang);
  updateLanguageControl(lang);
}

function updateLanguageControl(lang) {
  const languageButton = document.getElementById("languageToggle");
  const languageFlag = document.getElementById("languageFlag");
  const languageOptions = document.querySelectorAll(".language-option");

  const currentLang = getValidLanguage(lang);
  const meta = languageMeta[currentLang] || languageMeta.nl;

  if (languageFlag) {
    languageFlag.src = meta.flag;
    languageFlag.alt = meta.alt;
  }

  if (languageButton) {
    languageButton.setAttribute("aria-label", `Taal kiezen: ${meta.label}`);
    languageButton.setAttribute("title", `Taal kiezen: ${meta.label}`);
    languageButton.dataset.currentLanguage = currentLang;
  }

  languageOptions.forEach((option) => {
    const isActive = option.dataset.lang === currentLang;
    option.classList.toggle("active", isActive);
    option.setAttribute("aria-current", isActive ? "true" : "false");
  });
}

function closeLanguageMenu() {
  const languageButton = document.getElementById("languageToggle");
  const languageMenu = document.getElementById("languageMenu");
  if (!languageButton || !languageMenu) return;
  languageMenu.classList.remove("open");
  languageButton.setAttribute("aria-expanded", "false");
}

function toggleLanguageMenu() {
  const languageButton = document.getElementById("languageToggle");
  const languageMenu = document.getElementById("languageMenu");
  if (!languageButton || !languageMenu) return;
  const isOpen = languageMenu.classList.toggle("open");
  languageButton.setAttribute("aria-expanded", String(isOpen));
}
function translatePage(lang) {
  lang = getValidLanguage(lang);
  const languageMap = translations[lang] || translations.nl;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("is-rtl", lang === "ar");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const translation = getNestedValue(languageMap, key);
    if (translation !== undefined) {
      element.textContent = translation;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const translation = getNestedValue(languageMap, key);
    if (translation !== undefined) {
      element.placeholder = translation;
    }
  });

  document.querySelectorAll("[data-i18n-value]").forEach((element) => {
    const key = element.dataset.i18nValue;
    const translation = getNestedValue(languageMap, key);
    if (translation !== undefined) {
      element.value = translation;
    }
  });

  updateOtherJobVisibility();
}

function updateOtherJobVisibility() {
  const typeKlus = document.getElementById("typeKlus");
  const otherJobField = document.getElementById("otherJobField");
  const andersInput = document.getElementById("anders");
  if (!typeKlus || !otherJobField || !andersInput) {
    return;
  }

  const showOther = typeKlus.value === "Anders";
  otherJobField.classList.toggle("show", showOther);
  andersInput.required = showOther;
  if (!showOther) {
    andersInput.value = "";
  }
}

function translateText(key) {
  const lang = getCurrentLanguage();
  const translation = getNestedValue(
    translations[lang] || translations.nl,
    key,
  );
  return translation !== undefined ? translation : "";
}

document.addEventListener("DOMContentLoaded", () => {
  const menuToggle = document.querySelector(".menu-toggle");
  const navLinks = document.querySelector(".nav-links");
  const languageButton = document.getElementById("languageToggle");
  const languageMenu = document.getElementById("languageMenu");
  const languageOptions = document.querySelectorAll(".language-option");

  if (menuToggle && navLinks) {
    menuToggle.addEventListener("click", () => {
      const isOpen = navLinks.classList.toggle("open");
      menuToggle.setAttribute("aria-expanded", String(isOpen));
    });
  }

  if (languageButton && languageMenu) {
    languageButton.addEventListener("click", (event) => {
      event.preventDefault();
      event.stopPropagation();
      toggleLanguageMenu();
    });
  }

  languageOptions.forEach((option) => {
    option.addEventListener("click", (event) => {
      event.preventDefault();
      const selectedLanguage = option.dataset.lang;
      setLanguage(selectedLanguage);
      closeLanguageMenu();
    });
  });

  document.addEventListener("click", (event) => {
    const selector = document.querySelector(".language-selector");
    if (selector && !selector.contains(event.target)) {
      closeLanguageMenu();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeLanguageMenu();
    }
  });

  const backToTopLinks = document.querySelectorAll(".back-to-top");
  backToTopLinks.forEach((link) => {
    link.addEventListener("click", (event) => {
      event.preventDefault();

      window.scrollTo({
        top: 0,
        behavior: "smooth",
      });
    });
  });

  const revealItems = document.querySelectorAll(".reveal");
  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.14 },
  );

  revealItems.forEach((item) => revealObserver.observe(item));

  const typeKlus = document.getElementById("typeKlus");
  const otherJobField = document.getElementById("otherJobField");
  const andersInput = document.getElementById("anders");

  if (typeKlus && otherJobField && andersInput) {
    typeKlus.addEventListener("change", () => {
      const showOther = typeKlus.value === "Anders";
      otherJobField.classList.toggle("show", showOther);
      andersInput.required = showOther;
      if (!showOther) andersInput.value = "";
    });
  }

  const contactForm = document.getElementById("contactForm");
  const formMessage = document.getElementById("formMessage");

  if (contactForm && formMessage) {
    contactForm.addEventListener("submit", async (event) => {
      event.preventDefault();

      const accessKey = contactForm.querySelector(
        'input[name="access_key"]',
      )?.value;
      const submitButton = contactForm.querySelector('button[type="submit"]');

      if (!accessKey || accessKey === "YOUR_WEB3FORMS_ACCESS_KEY_HERE") {
        formMessage.textContent = translateText(
          "contact.messages.notConfigured",
        );
        formMessage.className = "form-message error";
        return;
      }

      const formData = new FormData(contactForm);
      submitButton.disabled = true;
      submitButton.textContent = translateText("contact.form.sending");
      formMessage.textContent = "";

      try {
        const response = await fetch(contactForm.action, {
          method: "POST",
          body: formData,
        });

        const result = await response.json();

        if (result.success) {
          formMessage.textContent = translateText("contact.messages.success");
          formMessage.className = "form-message success";
          contactForm.reset();
          otherJobField?.classList.remove("show");
        } else {
          formMessage.textContent = translateText("contact.messages.error");
          formMessage.className = "form-message error";
        }
      } catch (error) {
        formMessage.textContent = translateText(
          "contact.messages.networkError",
        );
        formMessage.className = "form-message error";
      } finally {
        submitButton.disabled = false;
        submitButton.textContent = translateText(submitButton?.dataset.i18n || "contact.form.submit");
      }
    });
  }

  setLanguage(getCurrentLanguage());
});
